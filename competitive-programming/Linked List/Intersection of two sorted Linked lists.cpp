Node *findIntersection(Node *head1, Node *head2)
{
    Node *ans = new Node(0);
    Node *temp = ans;
    while (head1 != NULL && head2 != NULL)
    {
        if (head1->data == head2->data)
        {
            Node *t = new Node(head1->data);
            temp->next = t;
            temp = temp->next;

            head1 = head1->next;
            head2 = head2->next;
        }
        else if (head1->data > head2->data)
        {
            head2 = head2->next;
        }
        else if (head2->data > head1->data)
        {
            head1 = head1->next;
        }
    }
    return ans->next;
}